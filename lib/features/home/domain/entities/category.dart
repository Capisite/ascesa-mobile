class Category {
  final String id;
  final String name;
  final String description;
  final String slug;
  final String image;
  final String type;
  final int order;

  Category({
    required this.id,
    required this.name,
    required this.description,
    required this.slug,
    required this.image,
    required this.type,
    required this.order,
  });
}
